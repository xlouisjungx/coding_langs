/**
 * Variable 선언하기
 *
 * 1) var - 더 이상 사용하지 않음.
 * 2) let
 * 3) const
 */

var name = 'xlouisjungx';
console.log(name);

var age = 21;
console.log(age);

let ive = '아이브';
console.log(ive);

// let과 var로 선언하면, 값을 추후 변경할 수 있음.
ive = '안유진';
console.log(ive);

// const는 추후 값을 변경할 수 없음.
const newJeans = '뉴진스';
console.log(newJeans);

